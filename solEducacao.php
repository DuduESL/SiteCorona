         <h1>A educação durante e após a pandemia: 5 ideias para navegar no ensino a distância</h1>
         <ol>
             <li>Conecte-se</li>
             <h4>Forme um canal de comunicação com pais, professores e alunos para reduzir a ansiedade e trazer informações corretas, confiáveis e de uma forma bem clara. Disponibilize o maior número possível de canais para que tudo seja amplamente divulgado, mas com a mesma mensagem para todos em um tom simples e direto. Pode ser nos grupos de redes sociais, no próprio site da escola ou através de e-mails. O ideal é que se estabeleça uma frequência para as atualizações e pessoas responsáveis para isso e, assim, desenvolva uma rede de confiança para os pais e alunos</h4>
             <li>Fortaleça a comunidade acadêmica</li>
             <h4>Estabeleça com os professores um canal bem claro e faça check-ins constantes, ou seja, combine encontros online e pergunte quais as infraestruturas que eles disponibilizam. Eles também necessitam de um repositório de conteúdo (um ponto de contato com recursos didáticos). Tudo isso cria um círculo de confiança e organização de trabalho. Recomenda-se uma mentoria entre os próprios professores que têm mais fluência digital com os que precisam de uma capacitação inicial.</h4>
             <li> Oriente os estudantes</li> 
             <h4>Eles precisam se organizar para as aulas online. Oriente-os e informe-os sobre as os hábitos e rotinas de estudo. É preciso que eles tenham uma sensação de proximidade com os professores e com a escola.</h4>
             <li>Estimule tarefas presenciais</li>
             <h4>Neste momento, não há nenhum problema em falar sobre tarefas presenciais como, por exemplo, ler um livro. Quem sabe, esta é uma grande oportunidade de incentivo à leitura e uma boa ocasião para enaltecer também o papel do bibliotecário da escola.</h4> 
             <li> Inove</li>
             <h4>Fazer o mesmo ou diferente? O que já se sabe ou com um pouco de ousadia? Mescle o tipo de atividade que você já realizava com uma nova. Busque seu repertório, mas aproveite o momento também para fazer algo diferente. Geralmente, a primeira semana é de acomodação. Na segunda, coloque pitadas de inovação. E, na medida em que se habitue com as novas ferramentas, inove mais.</h4>
         </ol>
