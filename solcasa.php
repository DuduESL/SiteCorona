       <h1>Cuidados com o coronavírus para quando precisar sair ou voltar para casa e no Trabalho</h1>
       <ul>
           <li>Tire as roupas e calçados no quintal (deixe sempre uma muda de roupa pronta ao sair);</li>
           <li>Se precisar trazer as roupas para dentro, coloque-as em um saco plástico;</li>
           <li>Se possível, lave as mãos na área externa da casa;</li>
           <li>Se não puder lavar as mãos antes de entrar, evite tocar nas coisas antes de lavar as mãos;</li>
           <li>Não cumprimente crianças, cônjuges ou outras pessoas que morem na casa antes de trocar de roupa e lavar as mãos;</li>
           <li>Se possível, tome banho assim que chegar;</li>
           <li>Desinfete celular, chaves e cartões bancários com álcool a 70%;</li>
           <li>Mochilas e bolsas de uso diário devem ficar em uma caixa, na área externa da casa;</li>
           <li>Limpe as embalagens que trouxe com solução de água sanitária (20 ml para cada 1 litro de água);</li>
           <li>Lave alimentos com água e sabão e os deixe 10 minutos imersos em solução de água sanitária (20 ml para cada 1 litro de água).</li>

        </ul>
        <h4>Além disso, no ambiente de trabalho, quem tem contato com clientes deve tomar algumas medidas, como aumentar a distância entre cliente e caixa, ter álcool gel disponível durante todo o expediente, controlar o fluxo de entrada de clientes para evitar filas e outras aglomerações.

            O uso de luvas e máscaras também deve ser considerado para garantir a proteção dos trabalhadores. “Esses serviços essenciais à sobrevivência não podem parar, mas seus trabalhadores precisam ser preservados o máximo possível”, defendeu Chioro.
            
            Outro cuidado importante para a população em geral é higienizar os produtos trazidos do supermercado. As embalagens podem ser limpas com uma solução de água sanitária. E os alimentos devem ser lavados e mergulhados em solução de hipoclorito ou água sanitária. O Ministério da Saúde mantém uma página com as principais atualizações sobre cuidados com o coronavírus.</h4>
